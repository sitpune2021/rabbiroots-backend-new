     * Display a listing of the resource.
     */
    public function index(Request $request)
    {
        $categories = Category::with('parent')
            ->active()
            ->when($request->level, fn ($q) =>
                $q->where('level', $request->level)
            )
            ->orderBy('sort_order')
            ->paginate($request->get('per_page', 10));

